const month = 3
switch (month) {
    case 1:
        console.log("April");
        
        break;
    case 2:
        console.log("May");
        
        break;
    case 3:
        console.log("Jun");
        
        break;
    case 4:
        console.log("July");
        
        break;
    case 5:
        console.log("August");
        
        break;
   

    default:
        console.log("Default Case Match");
        
        break;
}

